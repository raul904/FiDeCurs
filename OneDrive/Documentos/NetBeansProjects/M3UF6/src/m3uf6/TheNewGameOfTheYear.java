    private void vaciaArray() {
        for(int j=0; j<4; j++){
            for(int a=0; a<4; a++){
                info[j][a]=null;
            }
        }
    }

    private void interrogantes() {
        for(int j=0; j<4; j++){
            for(int i=0; i<4; i++){
               tabla.setValueAt("?", j, i);
            }
        }
    }

  
