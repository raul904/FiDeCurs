    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
       int row = rowClicked();
       int colum = columClicked();
       
      if(tabla.isEnabled()){
          
      
       if(tabla.getValueAt(row, colum)=="?"){
           tabla.setValueAt(info[row][colum],row, colum);
       
            if("W".equals(info[row][colum])){               
                contador = contador*2;
            }else if ("X".equals(info[row][colum])){
                JOptionPane.showConfirmDialog(null,"Has hecho: "+contador+ "Puntos",
                "The new Game of the year",
                JOptionPane.DEFAULT_OPTION,
                JOptionPane.PLAIN_MESSAGE);
                tabla.setEnabled(false);
                
            }else{
            contador++;
            }
               
          if(Integer.valueOf(recordPuntos.getText())<contador){
             recordPuntos.setText(String.valueOf(contador));
          
          }
         
       }
        jLabel3.setText(String.valueOf(contador));
        
      }
        System.out.println(contador);
    }//GEN-LAST:event_tablaMouseClicked

